package com.munir;

public class SwitchExamStrings {
    public static void main(String [] args){

        String color = "Green";

        switch (color){

            case "red":
            System.out.println(" the color is red");
            break;
            case "Green":
            System.out.println(" the color is green");
            break;
            default:
                System.out.println(" the color is not found");
                break;

        }
    }
}
