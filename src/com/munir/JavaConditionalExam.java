package com.munir;

public class JavaConditionalExam {
    public static void main(String[] args){

        int value1 = 1;
        int value2 = 2;
        int result;
        boolean somCondition = true;
        result = somCondition ? value1 : value2;
        System.out.println(result);
    }
}


