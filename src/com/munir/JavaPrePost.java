package com.munir;

public class JavaPrePost {
    public static void main(String[] args){
        int i = 3;
        i++;
        // Print 4
        System.out.println(i);
        ++i;
        // Print 5
        System.out.println(i);
        // Print 6
        System.out.println(++i);
        // Print 6
        System.out.println(i++);
        // Print 7
        System.out.println(i);
    }
}
